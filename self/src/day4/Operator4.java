package day4;

public class Operator4 {

	public static void main(String[] args) {
		//산술 연산자
		int v1 = 5;
		int v2 = 2;
		System.out.println(v1 + v2);	//7
		System.out.println(v1 - v2);	//3
		System.out.println(v1 * v2);	//10
		
		System.out.println(v1/v2);	//2   정수의 몫을 계산
		System.out.println(v1/(double)v2);  //2.5  실수 몫을 계산
		System.out.println(v1%v2);	//1   정수의 나머지를 계산
		
		
	}

}
